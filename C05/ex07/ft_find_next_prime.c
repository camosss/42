int	ft_is_prime(int nb)
{
	int i;

	if (nb <= 1)
		return (0);
	i = 2;
	while (i <= nb / i)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}

int	ft_find_next_prime(int nb)
{
	int i;

	if (ft_is_prime(nb) == 1)
		return (nb);
	i = 1;
	while (!ft_is_prime(nb + i))
		i++;
	return (nb + i);
}
